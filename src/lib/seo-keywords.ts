// Get keywords by search intent
export function getKeywordsByIntent(intent: 'navigational' | 'informational' | 'transactional' | 'commercial'): string[] {
  switch (intent) {
    case 'navigational':
      return SEO_KEYWORDS_BRAND;
    case 'informational':
      return [
        ...SEO_KEYWORDS_QUESTIONS,
        ...SEO_KEYWORDS_PROBLEMS,
        ...SEO_KEYWORDS_WORKFLOW,
        ...SEO_KEYWORDS_AI_TRENDING,
      ];
    case 'transactional':
      return [
        ...SEO_KEYWORDS_ACTION,
        ...SEO_KEYWORDS_LONGTAIL,
        ...SEO_KEYWORDS_USE_CASES,
      ];
    case 'commercial':
      return [
        ...SEO_KEYWORDS_ALTERNATIVES,
        ...SEO_KEYWORDS_BENEFITS,
        ...SEO_KEYWORDS_PRIMARY,
      ];
    default:
      return getAllKeywords();
  }
}

